package com.Clases;

public class Comuna {
    private int valorNodo;
    private int valorRecorrido;

    public Comuna(int nodo, int recorrido) {
        this.valorNodo = nodo;
        this.valorRecorrido = recorrido;
    }

    public int getValorNodo() {
        return valorNodo;
    }

    public int getValorRecorrido() {
        return valorRecorrido;
    }
}
